#!/usr/bin/env python3
# -*- coding: utf-8 -*-

import sys
import os
from dotenv import load_dotenv
from anthropic import Anthropic

# Windows文字コード対策
if sys.platform == 'win32':
    import io
    sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding='utf-8')

# .envファイルを読み込む
load_dotenv()

def test_text_analysis():
    """セッション記録テキストの分析テスト"""

    # セッション記録テキスト（YouTube自動字幕から貼り付け）
    session_transcript = """りえさん 2026-06-05 セッション記録

（セッション内容の要約）
- クライアント：りえさん、思春期の3人の子どもを育てているお母さん
- 主な相談内容：
  * 中学3年生の子が2月から学校に行けなくなった
  * 不登校のきっかけ：クラスメイトAくんからのいじめ
  * ご主人との対応方針の違い
  * 長男がこう君の不登校にイライラしている
  * こう君がゲームばかりしている

- セッションで出た主なテーマ：
  1. 不登校の原因の理解と親の対応方針
  2. 親のマインドセット（「子どもは必ず回復する」という信念）
  3. 親子間・夫婦間のコミュニケーション
  4. 自主公式（決められた期間は学校に行く努力をしない）
  5. 家を安全な場にすることの大切さ
  6. こう君との関係構築（料理、お出かけなど）

- モモさんからのアドバイス：
  * エネルギーの回復期では「責める」「促す」刺激を避けることが重要
  * 自主公式で「1週間休む」と決める → その間は親が学校の話をしない
  * 安全な場を確保することで、子どもの脳が回復モードになる
  * 家族会議で全員の合意を得ることが大切
"""

    print("[TEST] セッション分析テスト開始")
    print("=" * 60)
    print()

    # Anthropic クライアント初期化
    client = Anthropic()

    # ステップ1: セクション2-A（クライアント記録）の案を生成
    print("[STEP 1] セクション2-A（クライアント記録）の案を生成中...\n")

    message_2a = client.messages.create(
        model="claude-3-5-sonnet-20241022",
        max_tokens=2000,
        messages=[
            {
                "role": "user",
                "content": f"""以下のセッション記録をもとに、セクション2-A（クライアント記録）の以下の項目について、パターン①（概要+詳細版）形式で案を出してください。

【セッション記録】
{session_transcript}

【出力形式：パターン①（概要+詳細版）】
各項目について、以下のように出力してください：

【項目名】
■ 概要版（1行）
　→ 〇〇について気づいたこと

■ 詳細版（2～3行）
　→ セッション内での具体的な背景や経過を含める

---

【必須項目】
1. 課題（クライアントが自覚している課題 + セッション内で浮かり上がった問題点）
2. やってみようと思ったこと（クライアントが実践しようと思ったこと）
3. その他（自由記載）
4. 次回のフォローアップ予定日

各項目を上記形式で出力してください。クライアント名などの個人情報は含めないでください。
"""
            }
        ],
    )

    result_2a = message_2a.content[0].text
    print("[RESULT] セクション2-A（クライアント記録）の案")
    print("-" * 60)
    print(result_2a)
    print()

    # ステップ2: セクション2-B（セッション分析）の案を生成
    print("\n[STEP 2] セクション2-B（セッション分析）の案を生成中...\n")

    message_2b = client.messages.create(
        model="claude-3-5-sonnet-20241022",
        max_tokens=3000,
        messages=[
            {
                "role": "user",
                "content": f"""以下のセッション記録をもとに、セッション分析セクション（2-B）の7項目の案を、パターン①（概要+詳細版）形式で生成してください。

【セッション記録】
{session_transcript}

【出力形式：パターン①（概要+詳細版）】
各項目について、以下のように出力してください：

【項目名】
■ 概要版（1行）
　→ 〇〇について気づいたこと

■ 詳細版（2～3行）
　→ セッション内での具体的な背景、モモさん自身の考察を含める

---

【必須項目】
1. そのセッションで理解できたこと
2. 疑問に思ったこと
3. 自分の対応と迷い
4. クライアントの反応
5. 次回に向けてどうするか
6. 次回までに調べること
7. その他の気づき

各項目を上記形式で出力してください。クライアント名などの個人情報は含めないでください。
"""
            }
        ],
    )

    result_2b = message_2b.content[0].text
    print("[RESULT] セクション2-B（セッション分析）の案")
    print("-" * 60)
    print(result_2b)
    print()

    print("\n" + "=" * 60)
    print("[SUCCESS] テスト完了しました！")
    print()
    print("[EVALUATION]")
    print("- セクション2-Aの案の質：上記を確認してください")
    print("- セクション2-Bの案の質：上記を確認してください")
    print("- パターン①フォーマットは合っていますか？")
    print("- 個人名は含まれていませんか？")
    print()
    print("モモさんの評価をお聞かせください。")

if __name__ == "__main__":
    test_text_analysis()
