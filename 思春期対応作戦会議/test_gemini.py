#!/usr/bin/env python3
# -*- coding: utf-8 -*-

import sys
import os
from dotenv import load_dotenv
from google import genai

# Windows文字コード対策
if sys.platform == 'win32':
    import io
    sys.stdout = io.TextIOWrapper(sys.stdout.buffer, encoding='utf-8')

# .envファイルを読み込む
load_dotenv()

# セッション記録テキスト（YouTube字幕から）
SESSION_TRANSCRIPT = """
セッション日：2026-06-05
クライアント：思春期の子どもを3人育てるお母さん

【家族構成】
- 大学1年生（長男・せな君）
- 中学3年生（次男・こう君）：不登校
- 小学生（三男・空くん）
- 同居：自分の母親
- 夫のご両親は別居

【相談内容】
1. こう君（中3）が2月から学校に行けなくなった
   - きっかけ：同級生のA君からの継続的ないじめ・嫌がらせ
   - A君がゲームのアイテムを取った翌日から完全に行けなくなった
   - クラスは変更してもらった
   - 現在も学校へ行けない状態が続いている

2. 夫との意見の違い
   - 夫：「学校に行け」と時々言う
   - お母さん：言わない方針
   - 夫が帰宅すると「今日何してた？」と聞く→こう君が嫌がる
   - 夫に「言わないで」と頼んだが、聞いてくれるか不安

3. 長男（せな君）のイライラ
   - こう君のゲームの独り言がうるさいと言う
   - 直接言わずお母さんに言う
   - 壁を叩いたりする
   - こう君は長男が来たら別の部屋に行って自分を守れている

4. こう君の現状
   - リビングでゲーム（フォートナイト）を一日中している
   - 自分の部屋はない
   - お母さんとの関係は良好（相談できる）
   - 長男・三男とは話さない
   - オンラインゲームの友人ともあまり話せていない

【モモさんの対応とアドバイス】
- エネルギー回復の概念を説明（不登校の経過：エネルギー低下→回復→復帰）
- 安全な場の重要性：お父さんの「今日何してた？」がこう君の安全を脅かしている
- 「自主公式」の提案：期間を決めて（1週間）絶対に学校の話をしない→安心の中で回復させる
- 作戦会議的な取り組みの提案：家族で方向性を話し合う
- 参考書籍を紹介：「学校では教えてくれない 自分を休ませる方法」
- きょうだいへの対応：正しく状況を説明する
- こう君との関係構築案：料理（パン、プリン）、お出かけ（本人が計画）

【クライアントがやってみようと言ったこと】
1. 料理をこう君と一緒にする（パン、プリン、ウィンナーなど簡単なもの）
2. 出かける計画をこう君に立てさせる（行き先を選ばせる）
3. ゲームについてこう君の意見を聞く（「お母さんは少し減らした方がいいと思うけど、あなたはどう思う？」）
4. 相談したことをこう君に伝える（急に対応が変わるとびっくりするため）
5. 自主公式については夫とこう君の3人で話し合うことから始める
"""

def test_gemini_analysis():
    """Gemini API でセッション分析テスト"""

    print("[TEST] Gemini API セッション分析テスト開始")
    print("=" * 60)

    # Gemini クライアント初期化
    api_key = os.getenv("GEMINI_API_KEY")
    if not api_key:
        print("[ERROR] GEMINI_API_KEY が .env に設定されていません")
        return

    client = genai.Client(api_key=api_key)

    # ステップ1: セクション2-A（クライアント記録）の案を生成
    print("\n[STEP 1] セクション2-A（クライアント記録）の案を生成中...\n")

    prompt_2a = f"""以下のセッション記録をもとに、セクション2-A（クライアント記録）の以下の項目について、指定のフォーマットで案を出してください。

【セッション記録】
{SESSION_TRANSCRIPT}

【出力形式】
各項目について、以下のように出力してください：

【項目名】
■ 概要版（1行）
　→ 〇〇の内容

■ 詳細版（2～3行）
　→ セッション内での具体的な背景や経過を含める

---

【出力する項目】
1. 課題（クライアントが自覚している課題 + セッション内で浮かび上がった問題点）
2. やってみようと思ったこと（クライアントが実践しようと思ったこと）
3. その他（気になること・自由記載）
4. 次回のフォローアップ予定日（セッション内容から適切な時期を提案）

クライアント名などの個人情報は含めないでください。
日本語で出力してください。
"""

    response_2a = client.models.generate_content(
        model="gemini-2.0-flash-lite",
        contents=prompt_2a
    )

    print("[RESULT] セクション2-A（クライアント記録）の案")
    print("-" * 60)
    print(response_2a.text)

    # ステップ2: セクション2-B（セッション分析）の案を生成
    print("\n\n[STEP 2] セクション2-B（セッション分析）の案を生成中...\n")

    prompt_2b = f"""以下のセッション記録をもとに、相談者（保健師モモさん）の視点で、セッション分析セクション（2-B）の7項目の案を、指定のフォーマットで生成してください。

【セッション記録】
{SESSION_TRANSCRIPT}

【出力形式】
各項目について、以下のように出力してください：

【項目名】
■ 概要版（1行）
　→ 〇〇の内容

■ 詳細版（2～3行）
　→ セッション内での具体的な背景、保健師としての考察を含める

---

【出力する項目】
1. そのセッションで理解できたこと
2. 疑問に思ったこと
3. 自分の対応と迷い
4. クライアントの反応
5. 次回に向けてどうするか
6. 次回までに調べること
7. その他の気づき

クライアント名などの個人情報は含めないでください。
日本語で出力してください。
"""

    response_2b = client.models.generate_content(
        model="gemini-2.0-flash-lite",
        contents=prompt_2b
    )

    print("[RESULT] セクション2-B（セッション分析）の案")
    print("-" * 60)
    print(response_2b.text)

    print("\n" + "=" * 60)
    print("[SUCCESS] テスト完了しました！")
    print()
    print("【確認してください】")
    print("- セクション2-Aの案の質は十分ですか？")
    print("- セクション2-Bの案の質は十分ですか？")
    print("- フォーマットは合っていますか？")

if __name__ == "__main__":
    test_gemini_analysis()
